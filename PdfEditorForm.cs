        private Color GetNextColor()
        {
            double hue = (_colorIndex * 137.508) % 360.0;
            _colorIndex++;
            return HslToRgb(hue, 0.4, 0.85); // Saturation 40%, Lightness 85% for pastel colors
        }

        private Color HslToRgb(double h, double s, double l)
        {
            double c = (1.0 - Math.Abs(2.0 * l - 1.0)) * s;
            double x = c * (1.0 - Math.Abs((h / 60.0) % 2.0 - 1.0));
            double m = l - c / 2.0;

            double r = 0, g = 0, b = 0;
            if (h < 60) { r = c; g = x; }
            else if (h < 120) { r = x; g = c; }
            else if (h < 180) { g = c; b = x; }
            else if (h < 240) { g = x; b = c; }
            else if (h < 300) { r = x; b = c; }
            else { r = c; b = x; }

            return Color.FromArgb((int)((r + m) * 255), (int)((g + m) * 255), (int)((b + m) * 255));
        }

        public void AddPdfFiles(List<string> files, bool isAutoGenerated = true)
        {
            if (isAutoGenerated) _autoGeneratedFiles.AddRange(files);
            Color groupColor = GetNextColor();
            bool firstAdd = lstPages.Items.Count == 0;

            foreach (var file in files)
            {
                if (!File.Exists(file)) continue;
                try
                {
                    using (var doc = PdfReader.Open(file, PdfDocumentOpenMode.Import))
                    {
                        for (int i = 0; i < doc.PageCount; i++)
                        {
                            lstPages.Items.Add(new PdfPageItem
                            {
                                SourceFile = file,
                                PageIndex = i,
                                GroupColor = groupColor,
                                DisplayName = string.Format(L10n.T("page_name"), Path.GetFileName(file), i + 1)
                            });
                        }
                    }
                }
                catch { }
            }

            if (firstAdd && lstPages.Items.Count > 0)
                lstPages.SelectedIndex = 0;
        }

        private void LstPages_DrawItem(object sender, DrawItemEventArgs e)
        {
            if (e.Index < 0 || e.Index >= lstPages.Items.Count) return;

            var item = (PdfPageItem)lstPages.Items[e.Index];
            e.DrawBackground();

            // Tô màu nền theo group
            using (var brush = new SolidBrush(item.GroupColor))
            {
                e.Graphics.FillRectangle(brush, e.Bounds);
            }

            // Nếu đang chọn thì highlight đè lên
            if ((e.State & DrawItemState.Selected) == DrawItemState.Selected)
            {
                e.Graphics.FillRectangle(SystemBrushes.Highlight, e.Bounds);
            }

            var textColor = ((e.State & DrawItemState.Selected) == DrawItemState.Selected) ? SystemColors.HighlightText : SystemColors.ControlText;
            TextRenderer.DrawText(e.Graphics, item.DisplayName, e.Font, e.Bounds, textColor, TextFormatFlags.VerticalCenter | TextFormatFlags.Left);
            e.DrawFocusRectangle();
        }

        private void LstPages_SelectedIndexChanged(object sender, EventArgs e)
        {
            if (lstPages.SelectedItem is PdfPageItem item)
            {
                try
                {
                    var oldDoc = pdfViewer.Document;

                    using (var sourceDoc = PdfReader.Open(item.SourceFile, PdfDocumentOpenMode.Import))
                    using (var singlePageDoc = new PdfSharp.Pdf.PdfDocument())
                    {
                        singlePageDoc.AddPage(sourceDoc.Pages[item.PageIndex]);
                        var ms = new MemoryStream();
                        singlePageDoc.Save(ms);
                        ms.Position = 0;
                        var doc = PdfiumViewer.PdfDocument.Load(ms);
                        pdfViewer.Document = doc;
                    }

                    oldDoc?.Dispose();
                }
                catch { }
            }
        }

        private void BtnAdd_Click(object sender, EventArgs e)
        {
            using (var ofd = new OpenFileDialog { Filter = "PDF Files|*.pdf", Multiselect = true, Title = L10n.T("dlg_add_pdf") })
            {
                if (ofd.ShowDialog() == DialogResult.OK)
                {
                    AddPdfFiles(ofd.FileNames.ToList(), false);
                }
            }
        }

        private void BtnRemove_Click(object sender, EventArgs e)
        {
            var selected = lstPages.SelectedItems.Cast<PdfPageItem>().ToList();
            lstPages.ClearSelected();
            foreach (var item in selected)
                lstPages.Items.Remove(item);
        }

        private void BtnUp_Click(object sender, EventArgs e)
        {
            MoveSelectedItems(-1);
        }

        private void BtnDown_Click(object sender, EventArgs e)
        {
            MoveSelectedItems(1);
        }

        private void MoveSelectedItems(int direction)
        {
            if (lstPages.SelectedItems.Count == 0) return;

            var selectedItems = lstPages.SelectedItems.Cast<PdfPageItem>().ToList();
            var selectedIndices = lstPages.SelectedIndices.Cast<int>().ToList();

            if (direction == -1 && selectedIndices.Min() == 0) return;
            if (direction == 1 && selectedIndices.Max() == lstPages.Items.Count - 1) return;

            lstPages.BeginUpdate();
            lstPages.ClearSelected();

            if (direction == 1)
            {
                selectedIndices.Reverse();
                selectedItems.Reverse();
            }

            for (int i = 0; i < selectedIndices.Count; i++)
            {
                int index = selectedIndices[i];
                var item = selectedItems[i];
                lstPages.Items.RemoveAt(index);
                lstPages.Items.Insert(index + direction, item);
                lstPages.SetSelected(index + direction, true);
            }
            lstPages.EndUpdate();
        }

