        public void SetDefaultFileName(string name)
        {
            if (string.IsNullOrEmpty(txtFileName.Text))
                txtFileName.Text = name;
        }

        private void BtnSave_Click(object sender, EventArgs e)
        {
            if (lstPages.Items.Count == 0) return;

            using (var sfd = new SaveFileDialog { Filter = "PDF File|*.pdf", Title = L10n.T("dlg_save_pdf"), FileName = txtFileName.Text })
            {
                if (sfd.ShowDialog() == DialogResult.OK)
                {
                    try
                    {
                        using (var outDoc = new PdfSharp.Pdf.PdfDocument())
                        {
                            foreach (PdfPageItem item in lstPages.Items)
                            {
                                using (var inDoc = PdfReader.Open(item.SourceFile, PdfDocumentOpenMode.Import))
                                {
                                    outDoc.AddPage(inDoc.Pages[item.PageIndex]);
                                }
                            }
                            outDoc.Save(sfd.FileName);
                        }
                        MessageBox.Show(L10n.T("msg_save_success"), L10n.T("app_title"), MessageBoxButtons.OK, MessageBoxIcon.Information);
                        try { System.Diagnostics.Process.Start(sfd.FileName); } catch { }
                    }
                    catch (Exception ex)
                    {
                        MessageBox.Show(string.Format(L10n.T("msg_save_error"), ex.Message), L10n.T("err_title"), MessageBoxButtons.OK, MessageBoxIcon.Error);
                    }
                }
            }
        }

        private void LstPages_MouseDown(object sender, MouseEventArgs e)
        {
            if (e.Button == MouseButtons.Left)
                _dragStartPoint = e.Location;
        }

        private void LstPages_MouseMove(object sender, MouseEventArgs e)
        {
            if (e.Button == MouseButtons.Left && _dragStartPoint != Point.Empty)
            {
                if (Math.Abs(e.X - _dragStartPoint.X) > SystemInformation.DragSize.Width ||
                    Math.Abs(e.Y - _dragStartPoint.Y) > SystemInformation.DragSize.Height)
                {
                    var selected = lstPages.SelectedItems.Cast<PdfPageItem>().ToList();
                    if (selected.Count > 0)
                    {
                        lstPages.DoDragDrop(selected, DragDropEffects.Move);
                    }
                }
            }
        }

        private void LstPages_DragOver(object sender, DragEventArgs e)
        {
            e.Effect = DragDropEffects.Move;
        }

        private void LstPages_DragDrop(object sender, DragEventArgs e)
        {
            Point point = lstPages.PointToClient(new Point(e.X, e.Y));
            int index = lstPages.IndexFromPoint(point);
            if (index < 0) index = lstPages.Items.Count - 1;

            if (e.Data.GetDataPresent(typeof(List<PdfPageItem>)))
            {
                var itemsToMove = (List<PdfPageItem>)e.Data.GetData(typeof(List<PdfPageItem>));
                
                lstPages.BeginUpdate();
                lstPages.ClearSelected();
                foreach (var item in itemsToMove)
                    lstPages.Items.Remove(item);

                int insertIndex = index;
                if (insertIndex > lstPages.Items.Count) insertIndex = lstPages.Items.Count;

                for (int i = 0; i < itemsToMove.Count; i++)
                {
                    lstPages.Items.Insert(insertIndex + i, itemsToMove[i]);
                    lstPages.SetSelected(insertIndex + i, true);
                }
                lstPages.EndUpdate();
            }
            _dragStartPoint = Point.Empty;
        }

        protected override void OnFormClosed(FormClosedEventArgs e)
        {
            if (!_isSaved)
            {
                foreach (string file in _autoGeneratedFiles)
                {
                    try { if (File.Exists(file)) File.Delete(file); } catch { }
                }
            }
            _autoGeneratedFiles.Clear();
            _isSaved = false;
            lstPages.Items.Clear();
            
            if (Commands.MainFormInstance != null)
            {
                Commands.MainFormInstance.SetSubPlotMode(false);
                Commands.MainFormInstance.Show();
            }
            base.OnFormClosed(e);
        }
