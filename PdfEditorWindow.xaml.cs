        // ─── Save with Progress Bar ─────────────────────────────────────────
        private void BtnSave_Click(object sender, RoutedEventArgs e)
        {
            if (_pages.Count == 0) return;

            var sfd = new Microsoft.Win32.SaveFileDialog
            {
                Filter = "PDF File|*.pdf",
                Title = L10n.T("dlg_save_pdf"),
                FileName = txtFileName.Text
            };

            if (sfd.ShowDialog() == true)
            {
                try
                {
                    // Hiện progress bar
                    pnlProgress.Visibility = Visibility.Visible;
                    progressBar.Value = 0;
                    txtProgressLabel.Text = L10n.T("prog_merging");
                    txtProgressPercent.Text = "0%";
                    btnSave.IsEnabled = false;

                    // Force UI update
                    Dispatcher.Invoke(System.Windows.Threading.DispatcherPriority.Render, new Action(() => { }));

                    using (var outDoc = new PdfSharp.Pdf.PdfDocument())
                    {
                        int total = _pages.Count;
                        for (int i = 0; i < total; i++)
                        {
                            var item = _pages[i];
                            using (var inDoc = PdfReader.Open(item.SourceFile, PdfDocumentOpenMode.Import))
                            {
                                outDoc.AddPage(inDoc.Pages[item.PageIndex]);
                            }

                            // Cập nhật progress
                            int percent = (int)((double)(i + 1) / total * 100);
                            progressBar.Value = percent;
                            txtProgressPercent.Text = $"{percent}%";
                            txtProgressLabel.Text = string.Format(L10n.T("prog_progress"), i + 1, total);

                            // Force UI refresh
                            Dispatcher.Invoke(System.Windows.Threading.DispatcherPriority.Render, new Action(() => { }));
                        }

                        outDoc.Save(sfd.FileName);
                    }

                    // Hoàn tất
                    progressBar.Value = 100;
                    txtProgressPercent.Text = "100%";
                    txtProgressLabel.Text = "✅ " + L10n.T("msg_save_success");
                    _isSaved = true;

                    try { System.Diagnostics.Process.Start(sfd.FileName); } catch { }
                }
                catch (Exception ex)
                {
                    MessageBox.Show(
                        string.Format(L10n.T("msg_save_error"), ex.Message),
                        L10n.T("err_title"),
                        MessageBoxButton.OK, MessageBoxImage.Error);
                }
                finally
                {
                    btnSave.IsEnabled = true;
                    // Ẩn progress bar sau 2 giây
                    var timer = new System.Windows.Threading.DispatcherTimer { Interval = TimeSpan.FromSeconds(2) };
                    timer.Tick += (s2, e2) =>
                    {
                        pnlProgress.Visibility = Visibility.Collapsed;
                        timer.Stop();
                    };
                    timer.Start();
                }
            }
        }

        private void BtnCancel_Click(object sender, RoutedEventArgs e) => Close();

        // ─── Multi-item Drag & Drop ─────────────────────────────────────────

        private void LstPages_PreviewMouseLeftButtonDown(object sender, MouseButtonEventArgs e)
        {
            _dragStartPoint = e.GetPosition(lstPages);
            _isDragging = false;
        }

        private void LstPages_PreviewMouseMove(object sender, MouseEventArgs e)
        {
            if (e.LeftButton != MouseButtonState.Pressed) return;

            Point currentPos = e.GetPosition(lstPages);
            Vector diff = _dragStartPoint - currentPos;

            // Kiểm tra ngưỡng kéo
            if (Math.Abs(diff.X) > SystemParameters.MinimumHorizontalDragDistance ||
                Math.Abs(diff.Y) > SystemParameters.MinimumVerticalDragDistance)
            {
                if (_isDragging) return;
                _isDragging = true;

                var selectedItems = lstPages.SelectedItems.Cast<PdfPageViewModel>().ToList();
                if (selectedItems.Count == 0) return;

                var dragData = new DataObject("PdfPages", selectedItems);
                DragDrop.DoDragDrop(lstPages, dragData, DragDropEffects.Move);
                _isDragging = false;
            }
        }

        private void LstPages_DragOver(object sender, DragEventArgs e)
        {
            if (e.Data.GetDataPresent("PdfPages"))
                e.Effects = DragDropEffects.Move;
            else
                e.Effects = DragDropEffects.None;
            e.Handled = true;
        }

        private void LstPages_Drop(object sender, DragEventArgs e)
        {
            if (!e.Data.GetDataPresent("PdfPages")) return;

            var droppedItems = e.Data.GetData("PdfPages") as List<PdfPageViewModel>;
            if (droppedItems == null || droppedItems.Count == 0) return;

            // Tìm vị trí drop
            int dropIndex = _pages.Count; // mặc định cuối list
            var targetElement = e.OriginalSource as DependencyObject;

            while (targetElement != null)
            {
                if (targetElement is ListViewItem listViewItem)
                {
                    var targetItem = listViewItem.Content as PdfPageViewModel;
                    if (targetItem != null)
                    {
                        dropIndex = _pages.IndexOf(targetItem);
                    }
                    break;
                }
                targetElement = VisualTreeHelper.GetParent(targetElement);
            }

            // Xoá items cũ
            foreach (var item in droppedItems)
                _pages.Remove(item);

            // Điều chỉnh dropIndex sau khi xoá
            if (dropIndex > _pages.Count) dropIndex = _pages.Count;

            // Chèn lại tại vị trí mới
            for (int i = 0; i < droppedItems.Count; i++)
                _pages.Insert(dropIndex + i, droppedItems[i]);

            RefreshSequenceNumbers();

            // Giữ selection
            lstPages.SelectedItems.Clear();
            foreach (var item in droppedItems)
                lstPages.SelectedItems.Add(item);
        }

        // ─── Helpers ────────────────────────────────────────────────────────

        /// <summary>Cập nhật cột STT cho toàn bộ danh sách.</summary>
        private void RefreshSequenceNumbers()
        {
            for (int i = 0; i < _pages.Count; i++)
                _pages[i].SequenceNumber = i + 1;
        }

        /// <summary>Tạo màu pastel theo golden angle để phân biệt nhóm PDF.</summary>
        private System.Drawing.Color GetNextColor()
        {
            double hue = (_colorIndex * 137.508) % 360.0;
            _colorIndex++;
            return HslToRgb(hue, 0.4, 0.85);
        }

        private System.Drawing.Color HslToRgb(double h, double s, double l)
        {
            double c = (1.0 - Math.Abs(2.0 * l - 1.0)) * s;
            double x = c * (1.0 - Math.Abs((h / 60.0) % 2.0 - 1.0));
            double m = l - c / 2.0;

            double r = 0, g = 0, b = 0;
            if (h < 60) { r = c; g = x; }
            else if (h < 120) { r = x; g = c; }
            else if (h < 180) { g = c; b = x; }
            else if (h < 240) { g = x; b = c; }
            else if (h < 300) { r = x; b = c; }
            else { r = c; b = x; }

            return System.Drawing.Color.FromArgb(
                (int)((r + m) * 255),
                (int)((g + m) * 255),
                (int)((b + m) * 255));
        }

        // ─── Window Lifecycle ───────────────────────────────────────────────

        protected override void OnClosed(EventArgs e)
        {
            // Cleanup PDF viewer
            try
            {
                var oldDoc = _pdfViewer?.Document;
                if (_pdfViewer != null) _pdfViewer.Document = null;
                oldDoc?.Dispose();
            }
            catch { }

            // Xoá file tạm nếu chưa save
            if (!_isSaved)
            {
                foreach (string file in _autoGeneratedFiles)
                {
                    try { if (File.Exists(file)) File.Delete(file); } catch { }
                }
            }
            _autoGeneratedFiles.Clear();
            _isSaved = false;
            _pages.Clear();
            _instance = null;

            // Trở về MainForm
            if (Commands.MainFormInstance != null)
            {
                Commands.MainFormInstance.SetSubPlotMode(false);
                Commands.MainFormInstance.Show();
            }

            base.OnClosed(e);
        }
    }
