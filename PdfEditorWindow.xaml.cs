        private void BtnRemove_Click(object sender, RoutedEventArgs e)
        {
            var selected = lstPages.SelectedItems.Cast<PdfPageViewModel>().ToList();
            if (selected.Count == 0) return;

            // Ghi lại index trước khi xóa để chọn lại trang hợp lệ sau đó
            int firstSelectedIndex = lstPages.SelectedIndex;

            _isReordering = true;
            try
            {
                foreach (var item in selected)
                    _pages.Remove(item);

                RefreshSequenceNumbers();

                // ═══ FIX: sau khi xoá, chọn trang gần nhất (tránh preview kẹt) ═══
                if (_pages.Count > 0)
                {
                    int newIndex = Math.Min(firstSelectedIndex, _pages.Count - 1);
                    lstPages.SelectedIndex = newIndex;
                }
                else
                {
                    // Không còn trang nào → clear preview
                    var oldDoc = _pdfViewer.Document;
                    _pdfViewer.Document = null;
                    oldDoc?.Dispose();
                    txtNoPreview.Visibility = Visibility.Visible;
                }
            }
            finally
            {
                _isReordering = false;
                if (_pages.Count > 0)
                    LstPages_SelectionChanged(lstPages, null);
            }
        }

        private void BtnUp_Click(object sender, RoutedEventArgs e) => MoveSelectedItems(-1);
        private void BtnDown_Click(object sender, RoutedEventArgs e) => MoveSelectedItems(1);

        private void BtnRotate_Click(object sender, RoutedEventArgs e)
        {
            var selected = lstPages.SelectedItems.Cast<PdfPageViewModel>().ToList();
            if (selected.Count == 0) return;

            foreach (var item in selected)
            {
                item.RotationDelta = (item.RotationDelta + 90) % 360;

                // Update preview data to show rotation immediately
                try
                {
                    using (var ms = new MemoryStream(item.PreviewData))
                    using (var doc = PdfReader.Open(ms, PdfDocumentOpenMode.Modify))
                    {
                        foreach (var page in doc.Pages)
                        {
                            page.Rotate = (page.Rotate + 90) % 360;
                        }
                        using (var outMs = new MemoryStream())
                        {
                            doc.Save(outMs);
                            item.PreviewData = outMs.ToArray();
                        }
                    }
                }
                catch (Exception ex)
                {
                    System.Diagnostics.Debug.WriteLine($"[PdfEditor] Rotate error: {ex.Message}");
                }
            }

            // Force refresh preview if a rotated item is currently viewed
            LstPages_SelectionChanged(lstPages, null);
        }

        private void MoveSelectedItems(int direction)
        {
            var selectedIndices = new List<int>();
            foreach (var item in lstPages.SelectedItems)
            {
                int idx = _pages.IndexOf((PdfPageViewModel)item);
                if (idx >= 0) selectedIndices.Add(idx);
            }
            if (selectedIndices.Count == 0) return;

            selectedIndices.Sort();

            if (direction == -1 && selectedIndices[0] == 0) return;
            if (direction == 1 && selectedIndices[selectedIndices.Count - 1] == _pages.Count - 1) return;

            // Di chuyển theo thứ tự phù hợp
            if (direction == 1) selectedIndices.Reverse();

            _isReordering = true;
            try
            {
                var newIndices = new List<int>();
                foreach (int idx in selectedIndices)
                {
                    int newIdx = idx + direction;
                    _pages.Move(idx, newIdx);
                    newIndices.Add(newIdx);
                }

                RefreshSequenceNumbers();

                // Giữ lại selection
                lstPages.SelectedItems.Clear();
                foreach (int idx in newIndices)
                    lstPages.SelectedItems.Add(_pages[idx]);
            }
            finally
            {
                _isReordering = false;
                LstPages_SelectionChanged(lstPages, null);
            }
        }

