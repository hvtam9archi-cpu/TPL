        // ─── Page Selection → Preview ───────────────────────────────────────
        private void LstPages_SelectionChanged(object sender, SelectionChangedEventArgs e)
        {
            if (_isReordering) return;

            if (lstPages.SelectedItem is PdfPageViewModel item)
            {
                try
                {
                    var oldDoc = _pdfViewer.Document;

                    // Load from cached bytes — no file I/O, instant preview
                    var ms = new MemoryStream(item.PreviewData);
                    var pdfDoc = PdfiumViewer.PdfDocument.Load(ms);
                    _pdfViewer.Document = pdfDoc;

                    oldDoc?.Dispose();
                    txtNoPreview.Visibility = Visibility.Collapsed;
                }
                catch (Exception ex)
                {
                    System.Diagnostics.Debug.WriteLine($"[PdfEditor] Preview error: {ex.Message}");
                }
            }
            else
            {
                // Không có trang nào được chọn → hiện placeholder
                txtNoPreview.Visibility = Visibility.Visible;
            }
        }

