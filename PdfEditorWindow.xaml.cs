    /// <summary>
    /// Model cho mỗi trang PDF trong danh sách.
    /// Implements INotifyPropertyChanged để cập nhật STT khi thay đổi vị trí.
    /// </summary>
    public class PdfPageViewModel : INotifyPropertyChanged
    {
        private int _sequenceNumber;

        public string SourceFile { get; set; }
        public int PageIndex { get; set; }
        public System.Drawing.Color GroupColor { get; set; }
        public string DisplayName { get; set; }

        public int SequenceNumber
        {
            get => _sequenceNumber;
            set { _sequenceNumber = value; OnPropertyChanged(nameof(SequenceNumber)); }
        }

        /// <summary>Brush WPF dùng để hiển thị color indicator trong ListView.</summary>
        public SolidColorBrush GroupColorBrush =>
            new SolidColorBrush(Color.FromArgb(GroupColor.A, GroupColor.R, GroupColor.G, GroupColor.B));

        public event PropertyChangedEventHandler PropertyChanged;
        protected void OnPropertyChanged(string name) =>
            PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(name));
    }

    /// <summary>
    /// PDF Editor WPF Window — thay thế PdfEditorForm (WinForms).
    /// Tính năng: STT column, multi-drag-drop reorder, progress bar khi save,
    /// fix preview kẹt khi remove, dark theme #181A1F, custom scrollbar.
    /// </summary>
    public partial class PdfEditorWindow : Window
    {
        // ─── Singleton ──────────────────────────────────────────────────────
        private static PdfEditorWindow _instance;
        public static PdfEditorWindow Instance
        {
            get
            {
                if (_instance == null || !_instance.IsLoaded)
                    _instance = new PdfEditorWindow();
                return _instance;
            }
        }

        // ─── Data ───────────────────────────────────────────────────────────
        private readonly ObservableCollection<PdfPageViewModel> _pages = new ObservableCollection<PdfPageViewModel>();
        private int _colorIndex = 0;
        private bool _isSaved = false;
        private readonly List<string> _autoGeneratedFiles = new List<string>();

        // Drag-drop state
        private Point _dragStartPoint;
        private bool _isDragging = false;

        // PDF Viewer (WinForms control hosted via WindowsFormsHost)
        private PdfViewer _pdfViewer;

        // ─── Constructor ────────────────────────────────────────────────────
        private PdfEditorWindow()
        {
            InitializeComponent();

            // Tạo PdfViewer (WinForms) và host trong WPF
            _pdfViewer = new PdfViewer
            {
                ShowToolbar = false,
                ShowBookmarks = false,
                Dock = System.Windows.Forms.DockStyle.Fill
            };
            pdfHost.Child = _pdfViewer;

            lstPages.ItemsSource = _pages;
        }

        // ─── Public API ─────────────────────────────────────────────────────

        /// <summary>Thêm danh sách file PDF vào editor.</summary>
        public void AddPdfFiles(List<string> files, bool isAutoGenerated = true)
        {
            if (isAutoGenerated) _autoGeneratedFiles.AddRange(files);

            System.Drawing.Color groupColor = GetNextColor();
            bool firstAdd = _pages.Count == 0;

            foreach (string file in files)
            {
                if (!File.Exists(file)) continue;
                try
                {
                    using (var doc = PdfReader.Open(file, PdfDocumentOpenMode.Import))
                    {
                        for (int i = 0; i < doc.PageCount; i++)
                        {
                            _pages.Add(new PdfPageViewModel
                            {
                                SourceFile = file,
                                PageIndex = i,
                                GroupColor = groupColor,
                                DisplayName = string.Format(L10n.T("page_name"), Path.GetFileName(file), i + 1),
                                SequenceNumber = _pages.Count + 1
                            });
                        }
                    }
                }
                catch (Exception ex)
                {
                    System.Diagnostics.Debug.WriteLine($"[PdfEditor] Error loading {file}: {ex.Message}");
                }
            }

            if (firstAdd && _pages.Count > 0)
                lstPages.SelectedIndex = 0;
        }

        /// <summary>Đặt tên file mặc định (chỉ khi trống).</summary>
        public void SetDefaultFileName(string name)
        {
            if (string.IsNullOrEmpty(txtFileName.Text))
                txtFileName.Text = name;
        }

        // ─── Title bar (drag window) ────────────────────────────────────────
        private void TitleBar_MouseLeftButtonDown(object sender, MouseButtonEventArgs e)
        {
            if (e.ClickCount == 2)
            {
                // Double-click to maximize/restore
                WindowState = WindowState == WindowState.Maximized
                    ? WindowState.Normal
                    : WindowState.Maximized;
            }
            else
            {
                DragMove();
            }
        }

        private void BtnMinimize_Click(object sender, RoutedEventArgs e) => WindowState = WindowState.Minimized;
        private void BtnClose_Click(object sender, RoutedEventArgs e) => Close();

        // ─── Page Selection → Preview ───────────────────────────────────────
        private void LstPages_SelectionChanged(object sender, SelectionChangedEventArgs e)
        {
            if (lstPages.SelectedItem is PdfPageViewModel item)
            {
                try
                {
                    var oldDoc = _pdfViewer.Document;

                    using (var sourceDoc = PdfReader.Open(item.SourceFile, PdfDocumentOpenMode.Import))
                    using (var singlePageDoc = new PdfSharp.Pdf.PdfDocument())
                    {
                        singlePageDoc.AddPage(sourceDoc.Pages[item.PageIndex]);
                        var ms = new MemoryStream();
                        singlePageDoc.Save(ms);
                        ms.Position = 0;
                        var pdfDoc = PdfiumViewer.PdfDocument.Load(ms);
                        _pdfViewer.Document = pdfDoc;
                    }

                    oldDoc?.Dispose();
                    txtNoPreview.Visibility = Visibility.Collapsed;
                }
                catch (Exception ex)
                {
                    System.Diagnostics.Debug.WriteLine($"[PdfEditor] Preview error: {ex.Message}");
                }
            }
            else
            {
                // Không có trang nào được chọn → hiện placeholder
                txtNoPreview.Visibility = Visibility.Visible;
            }
        }

        // ─── Toolbar Actions ────────────────────────────────────────────────

        private void BtnAdd_Click(object sender, RoutedEventArgs e)
        {
            var ofd = new Microsoft.Win32.OpenFileDialog
            {
                Filter = "PDF Files|*.pdf",
                Multiselect = true,
                Title = L10n.T("dlg_add_pdf")
            };

            if (ofd.ShowDialog() == true)
            {
                AddPdfFiles(ofd.FileNames.ToList(), false);
            }
        }

        private void BtnRemove_Click(object sender, RoutedEventArgs e)
        {
            var selected = lstPages.SelectedItems.Cast<PdfPageViewModel>().ToList();
            if (selected.Count == 0) return;

            // Ghi lại index trước khi xóa để chọn lại trang hợp lệ sau đó
            int firstSelectedIndex = lstPages.SelectedIndex;

            foreach (var item in selected)
                _pages.Remove(item);

            RefreshSequenceNumbers();

            // ═══ FIX: sau khi xoá, chọn trang gần nhất (tránh preview kẹt) ═══
            if (_pages.Count > 0)
            {
                int newIndex = Math.Min(firstSelectedIndex, _pages.Count - 1);
                lstPages.SelectedIndex = newIndex;
            }
            else
            {
                // Không còn trang nào → clear preview
                var oldDoc = _pdfViewer.Document;
                _pdfViewer.Document = null;
                oldDoc?.Dispose();
                txtNoPreview.Visibility = Visibility.Visible;
            }
        }

        private void BtnUp_Click(object sender, RoutedEventArgs e) => MoveSelectedItems(-1);
        private void BtnDown_Click(object sender, RoutedEventArgs e) => MoveSelectedItems(1);

        private void MoveSelectedItems(int direction)
        {
            var selectedIndices = new List<int>();
            foreach (var item in lstPages.SelectedItems)
            {
                int idx = _pages.IndexOf((PdfPageViewModel)item);
                if (idx >= 0) selectedIndices.Add(idx);
            }
            if (selectedIndices.Count == 0) return;

            selectedIndices.Sort();

            if (direction == -1 && selectedIndices[0] == 0) return;
            if (direction == 1 && selectedIndices[selectedIndices.Count - 1] == _pages.Count - 1) return;

            // Di chuyển theo thứ tự phù hợp
            if (direction == 1) selectedIndices.Reverse();

            var newIndices = new List<int>();
            foreach (int idx in selectedIndices)
            {
                int newIdx = idx + direction;
                _pages.Move(idx, newIdx);
                newIndices.Add(newIdx);
            }

            RefreshSequenceNumbers();

            // Giữ lại selection
            lstPages.SelectedItems.Clear();
            foreach (int idx in newIndices)
                lstPages.SelectedItems.Add(_pages[idx]);
        }

