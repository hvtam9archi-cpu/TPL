        private readonly PdfPageViewModel _dummyItem = new PdfPageViewModel { SourceLabel = "DUMMY", SequenceNumber = 0, DisplayName = "" };

        /// <summary>Kiểm tra nếu element thuộc Scrollbar → không cho kéo.</summary>
        private bool IsOverScrollbar(DependencyObject element)
        {
            while (element != null)
            {
                if (element is System.Windows.Controls.Primitives.ScrollBar)
                    return true;
                element = VisualTreeHelper.GetParent(element);
            }
            return false;
        }

        private void LstPages_PreviewMouseLeftButtonDown(object sender, MouseButtonEventArgs e)
        {
            // Nếu click trên Scrollbar → bỏ qua, không bắt đầu drag
            if (IsOverScrollbar(e.OriginalSource as DependencyObject))
            {
                _isDragging = false;
                return;
            }
            _dragStartPoint = e.GetPosition(lstPages);
            _isDragging = false;
        }

        private void LstPages_PreviewMouseMove(object sender, MouseEventArgs e)
        {
            if (e.LeftButton != MouseButtonState.Pressed) return;

            // Nếu click ban đầu trên Scrollbar → bỏ qua
            if (IsOverScrollbar(e.OriginalSource as DependencyObject)) return;

            Point currentPos = e.GetPosition(lstPages);
            Vector diff = _dragStartPoint - currentPos;

            // Kiểm tra ngưỡng kéo
            if (Math.Abs(diff.X) > SystemParameters.MinimumHorizontalDragDistance ||
                Math.Abs(diff.Y) > SystemParameters.MinimumVerticalDragDistance)
            {
                if (_isDragging) return;
                _isDragging = true;

                var selectedItems = lstPages.SelectedItems.Cast<PdfPageViewModel>().ToList();
                if (selectedItems.Count == 0) return;

                var dragData = new DataObject("PdfPages", selectedItems);
                DragDrop.DoDragDrop(lstPages, dragData, DragDropEffects.Move);
                _isDragging = false;

                // Xoá dummy sau khi kéo xong (dù thành công hay huỷ)
                RemoveDropAdorner();
            }
        }

        private void LstPages_DragOver(object sender, DragEventArgs e)
        {
            if (e.Data.GetDataPresent("PdfPages"))
            {
                e.Effects = DragDropEffects.Move;
                UpdateDropAdorner(e);
            }
            else
                e.Effects = DragDropEffects.None;
            e.Handled = true;
        }

        /// <summary>
        /// Chèn dummy item để tạo khoảng trống đẩy các item khác xuống.
        /// Chỉ kích hoạt khi chuột nằm gần viền trên/dưới của 1 item (vùng ranh giới giữa 2 dòng).
        /// Khi chuột ở giữa item → xoá dummy để tránh nhấp nháy.
        /// </summary>
        private const double EdgeThreshold = 3; // px — vùng kích hoạt ở mỗi mép trên/dưới

        private void UpdateDropAdorner(DragEventArgs e)
        {
            var targetElement = e.OriginalSource as DependencyObject;
            ListViewItem targetListViewItem = null;

            while (targetElement != null)
            {
                if (targetElement is ListViewItem lvi)
                {
                    targetListViewItem = lvi;
                    break;
                }
                targetElement = VisualTreeHelper.GetParent(targetElement);
            }

            // Nếu đang hover trên chính dummy → giữ nguyên, không làm gì
            if (targetListViewItem != null)
            {
                var targetItem = targetListViewItem.Content as PdfPageViewModel;
                if (targetItem == _dummyItem) return;
            }

            // Nếu không tìm thấy ListViewItem (hover trên vùng trống cuối list)
            if (targetListViewItem == null)
            {
                // Kiểm tra nếu đang hover trên Header hoặc Scrollbar thì không nhảy xuống cuối
                var curr = e.OriginalSource as DependencyObject;
                while (curr != null)
                {
                    if (curr is GridViewColumnHeader || curr is System.Windows.Controls.Primitives.ScrollBar)
                        return;
                    curr = VisualTreeHelper.GetParent(curr);
                }
                // Chèn dummy ở cuối list
                int dropIndex = _pages.Count;
                int currentDummyIndex = _pages.IndexOf(_dummyItem);

                // Loại trừ dummy khi tính vị trí
                if (currentDummyIndex >= 0 && currentDummyIndex < dropIndex) dropIndex--;
                if (currentDummyIndex == dropIndex) return;

                _isReordering = true;
                try
                {
                    if (currentDummyIndex >= 0) _pages.Remove(_dummyItem);
                    if (dropIndex > _pages.Count) dropIndex = _pages.Count;
                    _pages.Insert(dropIndex, _dummyItem);
                }
                finally { _isReordering = false; }
                return;
            }

            // Xác định vị trí chuột trong item
            Point pos = e.GetPosition(targetListViewItem);
            double itemHeight = targetListViewItem.ActualHeight;
            bool nearTop = pos.Y < EdgeThreshold;
            bool nearBottom = pos.Y > (itemHeight - EdgeThreshold);

            // Chuột ở giữa item (dead zone) → xoá dummy nếu đang có
            if (!nearTop && !nearBottom)
            {
                RemoveDropAdorner();
                return;
            }

            // Chuột gần mép → tính dropIndex
            var hoveredItem = targetListViewItem.Content as PdfPageViewModel;
            if (hoveredItem == null) return;

            int hoveredIndex = _pages.IndexOf(hoveredItem);
            int newDropIndex = nearTop ? hoveredIndex : hoveredIndex + 1;

            int curDummyIdx = _pages.IndexOf(_dummyItem);

            // Điều chỉnh nếu dummy đang đứng trước dropIndex
            if (curDummyIdx >= 0 && curDummyIdx < newDropIndex)
                newDropIndex--;

            // Nếu vị trí không đổi → bỏ qua
            if (curDummyIdx == newDropIndex) return;

            _isReordering = true;
            try
            {
                if (curDummyIdx >= 0) _pages.Remove(_dummyItem);

                if (newDropIndex < 0) newDropIndex = 0;
                if (newDropIndex > _pages.Count) newDropIndex = _pages.Count;

                _pages.Insert(newDropIndex, _dummyItem);
            }
            finally
            {
                _isReordering = false;
            }
        }

        private void RemoveDropAdorner()
        {
            if (_pages.Contains(_dummyItem))
            {
                _isReordering = true;
                _pages.Remove(_dummyItem);
                _isReordering = false;
            }
        }

        private void LstPages_Drop(object sender, DragEventArgs e)
        {
            int dummyIndex = _pages.IndexOf(_dummyItem);
            RemoveDropAdorner();

            if (!e.Data.GetDataPresent("PdfPages")) return;

            var droppedItems = e.Data.GetData("PdfPages") as List<PdfPageViewModel>;
            if (droppedItems == null || droppedItems.Count == 0) return;

            int dropIndex = dummyIndex >= 0 ? dummyIndex : _pages.Count;

            _isReordering = true;
            try
            {
                // Tính xem có bao nhiêu item được thả nằm trước dropIndex để lùi vị trí chèn
                int shift = 0;
                foreach (var item in droppedItems)
                {
                    if (_pages.IndexOf(item) < dropIndex)
                        shift++;
                }
                dropIndex -= shift;

                // Xoá items cũ
                foreach (var item in droppedItems)
                    _pages.Remove(item);

                // Điều chỉnh dropIndex sau khi xoá
                if (dropIndex < 0) dropIndex = 0;
                if (dropIndex > _pages.Count) dropIndex = _pages.Count;

                // Chèn lại tại vị trí mới
                for (int i = 0; i < droppedItems.Count; i++)
                    _pages.Insert(dropIndex + i, droppedItems[i]);

                RefreshSequenceNumbers();

                // Giữ selection
                lstPages.SelectedItems.Clear();
                foreach (var item in droppedItems)
                    lstPages.SelectedItems.Add(item);
            }
            finally
            {
                _isReordering = false;
                LstPages_SelectionChanged(lstPages, null);
            }
        }

        // ─── Helpers ────────────────────────────────────────────────────────

        /// <summary>Cập nhật cột STT cho toàn bộ danh sách.</summary>
        private void RefreshSequenceNumbers()
        {
            for (int i = 0; i < _pages.Count; i++)
                _pages[i].SequenceNumber = i + 1;
            AutoResizeSequenceColumn();
        }

        /// <summary>Tự động điều chỉnh chiều rộng các cột cho phù hợp với nội dung.</summary>
        private void AutoResizeSequenceColumn()
        {
            if (_pages.Count == 0) return;

            // Tính chiều rộng cần thiết dựa trên số chữ số lớn nhất (cột STT)
            int maxDigits = _pages.Count.ToString().Length;
            double needed = Math.Max(35, maxDigits * 8 + 10);

            var gridView = lstPages.View as GridView;
            if (gridView != null && gridView.Columns.Count >= 4)
            {
                // Thay đổi chiều rộng cột #
                gridView.Columns[0].Width = needed;

                // Thay đổi chiều rộng cột Page để tự động fit chữ dài nhất
                if (double.IsNaN(gridView.Columns[3].Width))
                    gridView.Columns[3].Width = gridView.Columns[3].ActualWidth;

                gridView.Columns[3].Width = double.NaN; // Force Auto size
            }
        }

        /// <summary>Tạo màu pastel theo golden angle để phân biệt nhóm PDF.</summary>
        private System.Drawing.Color GetNextColor()
        {
            double hue = (_colorIndex * 137.508) % 360.0;
            _colorIndex++;
            return HslToRgb(hue, 0.4, 0.85);
        }

        private System.Drawing.Color HslToRgb(double h, double s, double l)
        {
            double c = (1.0 - Math.Abs(2.0 * l - 1.0)) * s;
            double x = c * (1.0 - Math.Abs((h / 60.0) % 2.0 - 1.0));
            double m = l - c / 2.0;

            double r = 0, g = 0, b = 0;
            if (h < 60) { r = c; g = x; }
            else if (h < 120) { r = x; g = c; }
            else if (h < 180) { g = c; b = x; }
            else if (h < 240) { g = x; b = c; }
            else if (h < 300) { r = x; b = c; }
            else { r = c; b = x; }

            return System.Drawing.Color.FromArgb(
                (int)((r + m) * 255),
                (int)((g + m) * 255),
                (int)((b + m) * 255));
        }

        // ─── Window Lifecycle ───────────────────────────────────────────────

        protected override void OnClosed(EventArgs e)
        {
            // Cleanup PDF viewer
            try
            {
                var oldDoc = _pdfViewer?.Document;
                if (_pdfViewer != null) _pdfViewer.Document = null;
                oldDoc?.Dispose();
            }
            catch { }

            // Xoá file tạm nếu chưa save
            if (!_isSaved)
            {
                foreach (string file in _autoGeneratedFiles)
                {
                    try { if (File.Exists(file)) File.Delete(file); } catch { }
                }
            }
            _autoGeneratedFiles.Clear();
            _isSaved = false;
            _colorIndex = 0;
            _groupIdCounter = 0;
            _pages.Clear();
            _instance = null;

            // Trở về MainForm
            if (Commands.MainFormInstance != null)
            {
                Commands.MainFormInstance.SetSubPlotMode(false);
                Commands.MainFormInstance.Show();
            }

            base.OnClosed(e);
        }
